#include "libft.h"

int	ft_putnbr(int n)
{
	char	nbr;
	int		printed;

	printed = ft_strlen(ft_itoa(n));
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	nbr = (n % 10) + '0';
	write(1, &nbr, 1);
	return (printed);
}
